function bgColor(type) {
  if (type == "success") {
    return "bg-[#ebfbf6]";
  } else if (type == "error") {
    return "bg-[#fdedee]";
  } else if (type == "warning") {
    return "bg-[#fff9e9]";
  } else {
    return "bg-[#fdedee]";
  }
}

function Alert({ alertMessage, type }) {
  return (
    <div
      className={`max-w-[30vw] min-w-[20vw] min-h-4 absolute ${bgColor(
        type
      )} left-[40%] top-5 py-2 px-4 flex items-center justify-between rounded`}
    >
      <div>{alertIcon(type)}</div>

      <p className="ml-2 text-black font-semibold break-words w-[90%]">
        {Boolean(alertMessage) ? alertMessage : ""}
      </p>
    </div>
  );
}

export default Alert;
